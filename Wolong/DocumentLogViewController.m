- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];
    
    // Configure the cell...
    cell.textLabel.text = [NSString stringWithFormat:@"2014年%d月15日 9:10", (int)indexPath.row+1];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"朋友%d 发给 朋友%d", (int)indexPath.row+1, (int)indexPath.row+2];
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    
    ChatViewController *controller = [self.storyboard instantiateViewControllerWithIdentifier:@"Chat"];
    if (controller != nil) {
        controller.userId = 6;
        [self.navigationController pushViewController:controller animated:YES];
    }
}

