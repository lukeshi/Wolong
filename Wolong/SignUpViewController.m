- (CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (indexPath.section == 0)
        return 40.0;
    return 60.0;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = nil;
    
    if (indexPath.section == 0) {
        cell = [tableView dequeueReusableCellWithIdentifier:@"InputCell" forIndexPath:indexPath];
        UITextField *field = (UITextField *)[cell viewWithTag:1];
        field.delegate = self;
        
        UIImageView *icon = (UIImageView *)[cell viewWithTag:100];

        if (indexPath.row == 0) {
            field.placeholder = @"Email / 手机号";
            icon.image = [UIImage imageNamed:@"person"];
        } else {
            field.placeholder = @"密码";
            field.secureTextEntry = YES;
            icon.image = [UIImage imageNamed:@"lock"];
        }
    } else {
        cell = [tableView dequeueReusableCellWithIdentifier:@"ButtonCell" forIndexPath:indexPath];
        cell.textLabel.text = @"注册";
    }
    
    return cell;
}

